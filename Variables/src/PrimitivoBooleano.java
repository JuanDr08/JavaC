public class PrimitivoBooleano {

    public static void main(String[] args) {




        // También conocido como tipo de dato lógico, ya que representa un valor true o false
        boolean datoLogico = true;
        boolean datoLogicoAsignado;

        double d = 98765.43e-3;
        float f = 1.2345e2f;

        // Mediante comparaciones
        datoLogicoAsignado = d < f;
        System.out.println("datoLogicoTrue = " + datoLogicoAsignado);

        // mediante operaciones y comparaciones
        boolean esIgual = 3-2 == 1;
        System.out.println("esIgual = " + esIgual);

    }

}
